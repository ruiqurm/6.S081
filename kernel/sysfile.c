// Create the path new as a link to the same inode as old.
uint64
sys_link(void)
{
  char name[DIRSIZ], new[MAXPATH], old[MAXPATH];
  struct inode *dp, *ip;

  if(argstr(0, old, MAXPATH) < 0 || argstr(1, new, MAXPATH) < 0)
    return -1;

  begin_op();
  if((ip = namei(old)) == 0){
    end_op();
    return -1;
  }

  ilock(ip);
  if(ip->type == T_DIR){
    iunlockput(ip);
    end_op();
    return -1;
  }

  ip->nlink++;
  iupdate(ip);
  iunlock(ip);

  if((dp = nameiparent(new, name)) == 0)
    goto bad;
  ilock(dp);
  if(dp->dev != ip->dev || dirlink(dp, name, ip->inum) < 0){
    iunlockput(dp);
    goto bad;
  }
  iunlockput(dp);
  iput(ip);

  end_op();

  return 0;

bad:
  ilock(ip);
  ip->nlink--;
  iupdate(ip);
  iunlockput(ip);
  end_op();
  return -1;
}

// Is the directory dp empty except for "." and ".." ?
static int
isdirempty(struct inode *dp)
{
  int off;
  struct dirent de;

  for(off=2*sizeof(de); off<dp->size; off+=sizeof(de)){
    if(readi(dp, 0, (uint64)&de, off, sizeof(de)) != sizeof(de))
      panic("isdirempty: readi");
    if(de.inum != 0)
      return 0;
  }
  return 1;
}

uint64
sys_unlink(void)
{
  struct inode *ip, *dp;
  struct dirent de;
  char name[DIRSIZ], path[MAXPATH];
  uint off;

  if(argstr(0, path, MAXPATH) < 0)
    return -1;

  begin_op();
  if((dp = nameiparent(path, name)) == 0){
    end_op();
    return -1;
  }

  ilock(dp);

  // Cannot unlink "." or "..".
  if(namecmp(name, ".") == 0 || namecmp(name, "..") == 0)
    goto bad;

  if((ip = dirlookup(dp, name, &off)) == 0)
    goto bad;
  ilock(ip);

  if(ip->nlink < 1)
    panic("unlink: nlink < 1");
  if(ip->type == T_DIR && !isdirempty(ip)){
    iunlockput(ip);
    goto bad;
  }

  memset(&de, 0, sizeof(de));
  if(writei(dp, 0, (uint64)&de, off, sizeof(de)) != sizeof(de))
    panic("unlink: writei");
  if(ip->type == T_DIR){
    dp->nlink--;
    iupdate(dp);
  }
  iunlockput(dp);

  ip->nlink--;
  iupdate(ip);
  iunlockput(ip);

  end_op();

  return 0;

bad:
  iunlockput(dp);
  end_op();
  return -1;
}

static struct inode*
create(char *path, short type, short major, short minor)
{
  struct inode *ip, *dp;
  char name[DIRSIZ];

  if((dp = nameiparent(path, name)) == 0)
    return 0;

  ilock(dp);

  if((ip = dirlookup(dp, name, 0)) != 0){
    iunlockput(dp);
    ilock(ip);
    if(type == T_FILE && (ip->type == T_FILE || ip->type == T_DEVICE))
      return ip;
    iunlockput(ip);
    return 0;
  }

  if((ip = ialloc(dp->dev, type)) == 0)
    panic("create: ialloc");

  ilock(ip);
  ip->major = major;
  ip->minor = minor;
  ip->nlink = 1;
  iupdate(ip);

  if(type == T_DIR){  // Create . and .. entries.
    dp->nlink++;  // for ".."
    iupdate(dp);
    // No ip->nlink++ for ".": avoid cyclic ref count.
    if(dirlink(ip, ".", ip->inum) < 0 || dirlink(ip, "..", dp->inum) < 0)
      panic("create dots");
  }

  if(dirlink(dp, name, ip->inum) < 0)
    panic("create: dirlink");

  iunlockput(dp);

  return ip;
}

uint64
sys_open(void)
{
  char path[MAXPATH];
  int fd, omode;
  struct file *f;
  struct inode *ip;
  int n;

  if((n = argstr(0, path, MAXPATH)) < 0 || argint(1, &omode) < 0)
    return -1;

  begin_op();

  if(omode & O_CREATE){
    ip = create(path, T_FILE, 0, 0);
    if(ip == 0){
      end_op();
      return -1;
    }
  } else {
    if((ip = namei(path)) == 0){
      end_op();
      return -1;
    }
    ilock(ip);
    if(ip->type == T_DIR && omode != O_RDONLY){
      iunlockput(ip);
      end_op();
      return -1;
    }
  }

  if(ip->type == T_DEVICE && (ip->major < 0 || ip->major >= NDEV)){
    iunlockput(ip);
    end_op();
    return -1;
  }

  if((f = filealloc()) == 0 || (fd = fdalloc(f)) < 0){
    if(f)
      fileclose(f);
    iunlockput(ip);
    end_op();
    return -1;
  }

  if(ip->type == T_DEVICE){
    f->type = FD_DEVICE;
    f->major = ip->major;
  } else {
    f->type = FD_INODE;
    f->off = 0;
  }
  f->ip = ip;
  f->readable = !(omode & O_WRONLY);
  f->writable = (omode & O_WRONLY) || (omode & O_RDWR);

  if((omode & O_TRUNC) && ip->type == T_FILE){
    itrunc(ip);
  }

  iunlock(ip);
  end_op();

  return fd;
}

uint64
sys_mkdir(void)
{
  char path[MAXPATH];
  struct inode *ip;

  begin_op();
  if(argstr(0, path, MAXPATH) < 0 || (ip = create(path, T_DIR, 0, 0)) == 0){
    end_op();
    return -1;
  }
  iunlockput(ip);
  end_op();
  return 0;
}

uint64
sys_mknod(void)
{
  struct inode *ip;
  char path[MAXPATH];
  int major, minor;

  begin_op();
  if((argstr(0, path, MAXPATH)) < 0 ||
     argint(1, &major) < 0 ||
     argint(2, &minor) < 0 ||
     (ip = create(path, T_DEVICE, major, minor)) == 0){
    end_op();
    return -1;
  }
  iunlockput(ip);
  end_op();
  return 0;
}

uint64
sys_chdir(void)
{
  char path[MAXPATH];
  struct inode *ip;
  struct proc *p = myproc();
  
  begin_op();
  if(argstr(0, path, MAXPATH) < 0 || (ip = namei(path)) == 0){
    end_op();
    return -1;
  }
  ilock(ip);
  if(ip->type != T_DIR){
    iunlockput(ip);
    end_op();
    return -1;
  }
  iunlock(ip);
  iput(p->cwd);
  end_op();
  p->cwd = ip;
  return 0;
}

uint64
sys_exec(void)
{
  char path[MAXPATH], *argv[MAXARG];
  int i;
  uint64 uargv, uarg;

  if(argstr(0, path, MAXPATH) < 0 || argaddr(1, &uargv) < 0){
    return -1;
  }
  memset(argv, 0, sizeof(argv));
  for(i=0;; i++){
    if(i >= NELEM(argv)){
      goto bad;
    }
    if(fetchaddr(uargv+sizeof(uint64)*i, (uint64*)&uarg) < 0){
      goto bad;
    }
    if(uarg == 0){
      argv[i] = 0;
      break;
    }
    argv[i] = kalloc();
    if(argv[i] == 0)
      goto bad;
    if(fetchstr(uarg, argv[i], PGSIZE) < 0)
      goto bad;
  }

  int ret = exec(path, argv);

  for(i = 0; i < NELEM(argv) && argv[i] != 0; i++)
    kfree(argv[i]);

  return ret;

 bad:
  for(i = 0; i < NELEM(argv) && argv[i] != 0; i++)
    kfree(argv[i]);
  return -1;
}

uint64
sys_pipe(void)
{
  uint64 fdarray; // user pointer to array of two integers
  struct file *rf, *wf;
  int fd0, fd1;
  struct proc *p = myproc();

  if(argaddr(0, &fdarray) < 0)
    return -1;
  if(pipealloc(&rf, &wf) < 0)
    return -1;
  fd0 = -1;
  if((fd0 = fdalloc(rf)) < 0 || (fd1 = fdalloc(wf)) < 0){
    if(fd0 >= 0)
      p->ofile[fd0] = 0;
    fileclose(rf);
    fileclose(wf);
    return -1;
  }
  if(copyout(p->pagetable, fdarray, (char*)&fd0, sizeof(fd0)) < 0 ||
     copyout(p->pagetable, fdarray+sizeof(fd0), (char *)&fd1, sizeof(fd1)) < 0){
    p->ofile[fd0] = 0;
    p->ofile[fd1] = 0;
    fileclose(rf);
    fileclose(wf);
    return -1;
  }
  return 0;
}

void
init_mmap_list(struct proc *proc){
  // initsleeplock(&proc->vma_lock,"mmap-lock");
  initlock(&proc->vma_cnt_lock,"mmap-count-lock");
  for(int i = 0; i <16;i++){
    proc->vma[i].valid = 0;
    initlock(&proc->vma[i].lock,"mmap-lock");
  }
  proc->vma_cnt = 0;
}

uint64 
sys_mmap(void){
  int length;
  int prot,flags;
  struct file* f;

  //ignore addr and offset
  if ( argint(1,&length) < 0 || argint(2,&prot) < 0 ||
       argint(3,&flags)  < 0 || argfd(4,0,&f) <  0){
         return -1; // parameter error
  }
  if(length%PGSIZE!=0)return -1;//要求长度对齐

  // 找一个可用的地址； 不考虑内存碎片，会一直向上分配
  static uint64 next_allocate_addr = MMAP_START;
  struct proc* proc = myproc();

  acquire(&proc->vma_cnt_lock);
  if(proc->vma_cnt==PROC_VMA_NUM - 1)panic("lack of vma");
  struct vma* p = &proc->vma[proc->vma_cnt];
  proc->vma_cnt++;
  release(&proc->vma_cnt_lock);

  acquire(&p->lock);
  
  // save information to struct 
  p->base = p->addr = next_allocate_addr;
  next_allocate_addr = PGROUNDUP(p->addr+length); 

  // 允许的几种情况：
  // [1] (READ  && (f->readable)) || !READ
  // [2] (WRITE && (f->writable || SHARED)) || !WRITE
  int r = (prot & PROT_READ);
  int w = (prot & PROT_WRITE);
  int x = (prot & PROT_EXEC);
  if (!( ((r && (f->readable)) || !r) &&
         ((w && (f->writable || (flags & MAP_PRIVATE) )) || !w) &&
         ((x && (f->readable)) || !x)
     )){
      //需要判断文件能否读写
      release(&p->lock);
      return -1;
    }
  filedup(f);  // add file referience
  p->file  = f;
  p->flags = flags;
  p->prot  =  (r  ? PTE_R : 0)  |
              (w  ? PTE_W : 0)  |
              (x  ? PTE_X : 0)  |
              PTE_U;
              // 括号不能省！
  p->length  = length;
  p->valid = 1;

  release(&p->lock);
  return (uint64)p->addr;
}

struct vma*
mmap_get(uint64 va){
  struct proc *proc = myproc();
  for(int i =0;i<PROC_VMA_NUM;i++){
    acquire(&proc->vma[i].lock);
    if(proc->vma[i].addr <= va && va < proc->vma[i].addr + proc->vma[i].length ){
      return &proc->vma[i];
    }
    release(&proc->vma[i].lock);
  }
  return 0;
}

int alloc_mmap_page(uint64 va){
  int n;
  struct proc* proc = myproc();
  uint64 mem;
  struct vma* p = mmap_get(va);
  if(p==0)return -1; // not found
  if ( (mem = (uint64)kalloc()) <= 0 ){
    panic("alloc_mmap_page: no remain space");
  }
  
  if(mappages(proc->pagetable, va, PGSIZE, mem, p->prot) != 0)
    panic("alloc_mmap_page: mappages error");
  struct inode *ip = p->file->ip;
  ilock(ip); 
  n= readi(ip, 1, va, va - p->base , PGSIZE);
  if(n<0)panic("alloc_mmap_page: read inode error");
  if(n!=PGSIZE){
    memset((void*)(mem+(uint64)n), 0, PGSIZE - n);
  }
  iunlock(ip);
  release(&p->lock);
  return 0;
}


uint64 
sys_munmap(void){
  uint64 va;
  int length;
  if ( argaddr(0,&va) < 0 ||argint(1,&length) < 0){
    return -1;
  }
  struct vma* p = mmap_get(va);
  struct proc* proc = myproc();
  if(p==0)return -1; // not found
  if(va % PGSIZE !=0 || length % PGSIZE!=0 || length < 0){
    release(&p->lock);
    return -1;
  }
  if(va + length > p->addr + p->length){ // out of range
    release(&p->lock);
    return -1;
  }

  p->addr   += length;
  p->length -= length;
  // printf("off1:%d\n",mycpu()->noff);
  // remove memory map;
  // if ip(inode pointer) is not NULL, then write it back
  release(&p->lock); // TODO:???
  lazy_uvmunmap(proc->pagetable,va,length,(p->flags & MAP_SHARED)?p->file->ip:0,p->base);  
  
  if(length ==0){
    p->valid = 0;
    fileclose(p->file);
  }
  // printf("off2:%d\n",mycpu()->noff);

  return 0;
}